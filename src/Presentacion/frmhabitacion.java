    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        if (txt_numero.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un numero de habitacion");
            txt_numero.requestFocus();
            return;
        }
        if (txt_descripcion.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una descripcion de la habitacion");
            txt_descripcion.requestFocus();
            return;
        }
        if (txt_precio_diario.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un precio_diario de la habitacion");
            txt_precio_diario.requestFocus();
            return;
        }
        if (txt_caracteristicas.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar unas caracteristicas de la habitacion");
            txt_caracteristicas.requestFocus();
            return;
        }
        
        vhabitacion dts = new vhabitacion();
        fhabitacion func = new fhabitacion();
        
        dts.setNumero(txt_numero.getText());
        
        int seleccionado = cmb_piso.getSelectedIndex();
        dts.setPiso((String)cmb_piso.getItemAt(seleccionado));
        
        dts.setDescripcion(txt_descripcion.getText());
        dts.setCaracteristicas(txt_caracteristicas.getText());
        
        dts.setPrecio_diario(Double.parseDouble(txt_precio_diario.getText()));
        
        seleccionado = cmb_estado.getSelectedIndex();
        dts.setEstado((String)cmb_estado.getItemAt(seleccionado));
        
        seleccionado = cmb_tipo_habitacion.getSelectedIndex();
        dts.setTipo_habitacion((String)cmb_tipo_habitacion.getItemAt(seleccionado));
        
        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La habitacion fue registrada satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }else if (accion.equals("editar")) {
            dts.setIdhabitacion(Integer.parseInt(txt_id_habitacion.getText()));
            
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La habitacion fue editada satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

