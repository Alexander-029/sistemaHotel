    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
        // TODO add your handling code here:
        btn_guardar.setText("Editar");
        habilitar();
        btn_eliminar.setEnabled(true);
        accion="editar";
        
        int fila = tablalistado.rowAtPoint(evt.getPoint());
        
        txt_id_habitacion.setText(tablalistado.getValueAt(fila, 0).toString());
        txt_numero.setText(tablalistado.getValueAt(fila, 1).toString());
        
        cmb_piso.setSelectedItem(tablalistado.getValueAt(fila, 2).toString());
        txt_descripcion.setText(tablalistado.getValueAt(fila, 3).toString());
        txt_caracteristicas.setText(tablalistado.getValueAt(fila, 4).toString());
        txt_precio_diario.setText(tablalistado.getValueAt(fila, 5).toString());
        
        cmb_estado.setSelectedItem(tablalistado.getValueAt(fila, 6).toString());
        cmb_tipo_habitacion.setSelectedItem(tablalistado.getValueAt(fila, 7).toString());
    }//GEN-LAST:event_tablalistadoMouseClicked

